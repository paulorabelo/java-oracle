/*
    while ( condição ) {
    Executa enquanto a condição é satisfeita
    }
*/

public class UsingLoops {
    public static void main (String args[]) {
        
        int numero = 1;

        while ( numero <= 5) {
            System.out.println("Executou " + numero + " vez");
            numero = numero + 1;
        }

    }
}

